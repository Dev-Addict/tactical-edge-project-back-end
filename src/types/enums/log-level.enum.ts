/**
 * Enum for log levels
 * @readonly
 * @enum {string}
 * @property {string} INFO - Info log level.
 * @property {string} DEBUG - Debug log level.
 * @property {string} WARN - Warn log level.
 * @property {string} ERROR - Error log level.
 */
export enum LogLevel {
	INFO = 'info',
	DEBUG = 'debug',
	WARN = 'warn',
	ERROR = 'error',
}
